package com.nit.bean;

public class Bike {
private String bikeNo;
private String bikeModel;
private String bikeYear;
private String bikeDesc;
private byte[] image;
private String baseStation;





public String getBaseStation() {
	return baseStation;
}
public void setBaseStation(String baseStation) {
	this.baseStation = baseStation;
}
public byte[] getImage() {
	return image;
}
public void setImage(byte[] image) {
	this.image = image;
}
public String getBikeNo() {
	return bikeNo;
}
public void setBikeNo(String bikeNo) {
	this.bikeNo = bikeNo;
}
public String getBikeModel() {
	return bikeModel;
}
public void setBikeModel(String bikeModel) {
	this.bikeModel = bikeModel;
}
public String getBikeYear() {
	return bikeYear;
}
public void setBikeYear(String bikeYear) {
	this.bikeYear = bikeYear;
}
public String getBikeDesc() {
	return bikeDesc;
}
public void setBikeDesc(String bikeDesc) {
	this.bikeDesc = bikeDesc;
}



}
